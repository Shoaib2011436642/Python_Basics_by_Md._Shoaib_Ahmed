a = 40
b = 80

if a > b:                               #If this condition turns out false then it will not execute the code falls under "if"
    print("a is greater than b")        #If the logical condition turns out true then it will execute the code
elif a==b:
    print("a is equal to b")            #elif used, when multiple logical conditions are present.
                                        #Here in the code it also turned out as false. So, code also didn't execute.
else:                                   #Here the codes fall under the else function executed as the if condition was false
    print("b is greater than a!")

