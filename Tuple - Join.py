tuple_1 = (1,2,3,4)
tuple_2 = (5,6,7)

tuple_3 = tuple_1 + tuple_2         #Merging two tuples

print(tuple_3)

print(tuple_1 + tuple_2)            #merging in 1 line of code, by not storing it in a variable

#Multiplying tuples
tuple_4 = tuple_1 * 2               #printing all the values two times in a single tuple
print(tuple_4)

print(tuple_1 * 2)                  #same using single line of code