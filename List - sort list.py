#sorting alphanumerically (ascending)
li_1 = [5, 2, 6, 4, 1]
li_2 = ["c", "a", "d", "b"]

li_1.sort()
print(li_1)

li_2.sort()
print(li_2)

#sorting reversely but alphanumerically (descending)
li_3 = [5, 2, 6, 4, 1]
li_3.sort(reverse = True)               #setting the condition inside sort function
print(li_3)

#reverse the list
list_4 = ["mango", "banana", "orange", "apple"]
list_4.reverse()
print(list_4)