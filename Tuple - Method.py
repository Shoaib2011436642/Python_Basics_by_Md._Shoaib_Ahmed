#Use of count( ) - Tells us the number of how many times a specific value is present in the tuple.

tuple_1 = (1,2,3,2,5,4,4,7,8,2,11)
x = tuple_1.count(2)
print(x)

#Use of index( ) - Tell us the index number of a specific value in the tuple

tuple_2 = ('messi', 'neymar', 'ronaldo', 'mbappe')
y = tuple_2.index('neymar')
print(y)
